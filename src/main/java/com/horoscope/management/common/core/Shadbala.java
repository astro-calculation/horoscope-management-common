package com.horoscope.management.common.core;

/**
 * Categories that contribute to the Shadbala (six-fold strength) calculation of
 * planets. This enumeration is intentionally compact and can be extended if the
 * consuming services need more granular values.
 */
public enum Shadbala {
  STHANA_BALA,
  DIG_BALA,
  KALA_BALA,
  CHESTA_BALA,
  NAISARGIKA_BALA,
  DRIG_BALA,
  TOTAL_BALA
}

